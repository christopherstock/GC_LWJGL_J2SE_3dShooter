    package de.christopherstock.shooter.game.wearpon;

    /**************************************************************************************
    *   The offset for the muzzle flash.
    *
    *   @author     Christopher Stock
    *   @version    0.3.10
    **************************************************************************************/
    public final class FireFXOffset
    {
        public int x = 0;
        public int y = 0;

        public FireFXOffset( int aX, int aY )
        {
            x = aX;
            y = aY;
        }
    }
